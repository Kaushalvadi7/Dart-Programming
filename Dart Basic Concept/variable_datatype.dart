void main() {
  int a = 10;
  double b = 20.5;
  String name = 'Dart';
  bool isFun = true;

  // Type conversion
  String ageAsString = a.toString();
  int heightAsInt = b.toInt();

  print('Integer: $a');
  print('Double: $b');
  print('String: $name');
  print('Boolean: $isFun');
  print('Converted String: $ageAsString');
  print('Converted Integer: $heightAsInt');
}
