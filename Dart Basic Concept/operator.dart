void main() {
  int a = 10, b = 5;

  // Arithmetic operators
  print('Addition: ${a + b}');
  print('Subtraction: ${a - b}');
  print('Multiplication: ${a * b}');
  print('Division: ${a / b}');

  // Relational operators
  print('a > b: ${a > b}');
  print('a < b: ${a < b}');

  // Logical operators
  print('a > 0 && b > 0: ${a > 0 && b > 0}');
  print('a > 0 || b < 0: ${a > 0 || b < 0}');
}
